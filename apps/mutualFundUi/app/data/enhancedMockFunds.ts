export const generateEnhancedChartData = () => {
  const historicalMonths = ['Jan 23', 'Apr 23', 'Jul 23', 'Oct 23', 'Jan 24', 'Apr 24', 'Jul 24', 'Oct 24', 'Dec 24'];
  const futureMonths = ['Mar 25', 'Jun 25', 'Sep 25', 'Dec 25'];
  
  const data = [];
  let navValue = 100;
  
  // Historical data
  for (const month of historicalMonths) {
    navValue = navValue * (1 + (Math.random() * 0.08 + 0.02));
    data.push({
      month,
      historicalNAV: parseFloat(navValue.toFixed(2)),
    });
  }
  
  // Future predictions
  let predictedValue = navValue;
  for (const month of futureMonths) {
    predictedValue = predictedValue * (1 + (Math.random() * 0.10 + 0.03));
    data.push({
      month,
      historicalNAV: null as any,
      predictedNAV: parseFloat(predictedValue.toFixed(2)),
    });
  }
  
  // Add connection point
  const lastHistoricalIndex = historicalMonths.length - 1;
  if (data[lastHistoricalIndex]) {
    data[lastHistoricalIndex].predictedNAV = data[lastHistoricalIndex].historicalNAV;
  }
  
  return data;
};